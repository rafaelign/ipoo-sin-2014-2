package Atividade4_0;


public class Data
{
    private int dia;
    private int mes;
    private int ano;
    
    public Data()
    {
        this.dia = 1;
        this.mes = 1;
        this.ano = 1;
    }

    public Data(int dia, int mes, int ano)
    {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }
}
